# <==================================================================================================>
#                                         IMPORTS
# <==================================================================================================>
import json


# <==================================================================================================>
#                                         READ JSON FILE
# <==================================================================================================>
def read_json_file(file_path: str) -> json:
    values_data = json.load(open(file_path))
    return values_data


# <==================================================================================================>
#                                         SAVE JSON FILE
# <==================================================================================================>
def save_json(file_path: str, data: json):
    with open(file_path, "w+") as file:
        json.dump(data, file, indent=4)
