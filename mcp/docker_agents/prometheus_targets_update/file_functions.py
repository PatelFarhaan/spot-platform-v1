# <==================================================================================================>
#                                          IMPORTS
# <==================================================================================================>
import json


# <==================================================================================================>
#                                          READ JSON
# <==================================================================================================>
def read_json(filename):
    with open(filename, 'r') as openfile:
        data = json.load(openfile)
    return data


# <==================================================================================================>
#                                          SAVE JSON
# <==================================================================================================>
def save_json(filename, data):
    with open(filename, "w") as outfile:
        json.dump(data, outfile, indent=4)
