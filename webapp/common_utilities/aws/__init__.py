# <==================================================================================================>
#                                         IMPORTS
# <==================================================================================================>
import os
import boto3


# <==================================================================================================>
#                                        DEFINE AWS REGION
# <==================================================================================================>
aws_region = os.getenv("AWS_REGION")


# <==================================================================================================>
#                                        GET AWS RESOURCE
# <==================================================================================================>
def get_boto3_resource(service_name: str) -> boto3.resource:
    resource = boto3.resource(service_name,
                              region_name=aws_region)
    return resource


# <==================================================================================================>
#                                          GET AWS CLIENT
# <==================================================================================================>
def get_boto3_client(service_name: str) -> boto3.client:
    client = boto3.client(service_name,
                          region_name=aws_region)
    return client
