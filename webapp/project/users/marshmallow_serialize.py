from project import ma
from flask_marshmallow import fields as fd


# <==================================================================================================>
#                                     INVESTOR USER SCHEMA
# <==================================================================================================>
class UserSchema(ma.Schema):
    id = fd.fields.String()

    class Meta:
        fields = ("first_name", "last_name", "email")
